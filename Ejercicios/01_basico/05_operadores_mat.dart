main() {
  // Operador de suma
  int a = 10 + 5;

  // Operador de resta
  a = 20 - 10;

  // Operador multiplicación
  a = 10 * 2;

  // Operador División
  double b = 5 / 2;

  // Operador modulo o resto
  b = -10.0 % 3;

  // Epresion menos volver un valor negativo
  b = -b;

  //Parte entera de una división
  int c = 10 ~/ 3; // 3

  // Operador asignación
  int d = 1;

  // Operador incremento en 1
  d++;

  // Operador decremento en 1
  d--;

  // Operador asignacion incremento
  d += 2;

  // Operador asignación decremento
  d -= 2;
}
